using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CalculatorClassLibrary
{
    public class Addition
    {

        /// <summary>
        /// Udfører addition af to tal.
        /// </summary>
        /// <param name="num1">Første tal til addition.</param>
        /// <param name="num2">Andet tal til addition.</param>
        /// <returns>Summen af de to tal.</returns>
        public double Add(double num1, double num2)
        {
            return num1 + num2;  // Returnerer summen af de to tal
        }


    }
}
